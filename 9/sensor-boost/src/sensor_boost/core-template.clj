(def phase-permutations (combo/permutations '(5 6 7 8 9)))

(defn initial-state [program-text in-channel out-channel]
  {:program-text program-text
   :in in-channel
   :out out-channel})

(defn load-input [in-channel val]
  (async/>!! in-channel val))

(defn make-amplifier [in-channel out-channel]
  (async/thread (b/compute (initial-state input in-channel out-channel))))

(defn run-permutation [permutation]
  (let [channels (vec (take 5 (repeatedly #(async/chan (async/buffer 3)))))
        ports (take 5 (partition 2 1 (cycle channels)))
        threads (map #(apply make-amplifier %) ports)]
    ;;doall forces map to not be lazy
    (doall (map #(apply load-input %) (partition 2 (interleave channels permutation))))
    (load-input (first channels) 0)
    (last (async/<!! (last threads)))))

(defn -main [& _args]
  (->> phase-permutations
     (map run-permutation)
     (apply max)
     (println)))
